package org.sayar.net.Model.Filter;

public class UserTypeFilter {
    private String name;
    private String role;

    public UserTypeFilter() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
